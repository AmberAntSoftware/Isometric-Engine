void ISO_clearSpriteCache(){
    int x,y,z;
    for(x=0;x<256;x++){
        for(y=0;y<256;y++){
            for(z=0;z<256;z++){
                ISO_grid[x][y][z]=0;
                ISO_Sprite *node = ISO_spriteCache[x][y][z];
                ISO_Sprite *next;
                while(node!=NULL){
                    next = node->next;
                    ISO_deleteSprite(node);
                }
                ISO_spriteCache[x][y][z]=NULL;
            }
        }
    }
}
void ISO_setMapData(int x, int y, int z, int w, int h, int dpth, short blockID){
    int xw=x+w;
    int yh=x+h;
    int zd=z+dpth;
    for(x=0;x<xw;x++){
        for(y=0;y<yh;y++){
            for(z=0;z<zd;z++){
            if(x>-1&&x<ISO_width&&
                y>-1&&y<ISO_height&&
                z>-1&&z<ISO_depth){
                    ISO_grid[x][y][z]=blockID;
                }
            }
        }
    }
}
void ISO_setBlockData(int x, int y, int z, short blockID){
    if(x>-1&&x<ISO_width&&
        y>-1&&y<ISO_height&&
        z>-1&&z<ISO_depth){
            ISO_grid[x][y][z]=blockID;
    }
}


